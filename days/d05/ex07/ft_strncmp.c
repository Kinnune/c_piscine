int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (s2[i] != '\0' && s1[j] != '\0')
	{
		while (s2[i] == s1[j] && s2[i] != '\0' && s1[j] != '\0' && (j + 1) < n)
		{
			i++;
			j++;
		}
		return (s1[j] - s2[i]);
	}
	return (s1[0] - s2[0]);
}
